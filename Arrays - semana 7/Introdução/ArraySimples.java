package Introducao;

public class ArraySimples {
    public static void main(String[] args) {
        int[] idade = new int[3];
        idade[0] = 18;
        idade[1] = 20;
        idade[2] = 23;

        System.out.println("Idade 1: " + idade[0]);
        System.out.println("Idade 2: " + idade[1]);
        System.out.println("Idade 3: " + idade[2]);
    }
}
